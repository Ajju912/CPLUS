#include <iostream>
using namespace std;

int main() {
    cout<<"SIZE OF INTEGER - "<<sizeof(int)<<" Bytes"<<"\n";
    cout<<"SIZE OF FLOAT - "<<sizeof(float)<<" Bytes"<<"\n";
    cout<<"SIZE OF DOUBLE - "<<sizeof(double)<<" Bytes"<<"\n";
    cout<<"SIZE OF CHAR - "<<sizeof(char)<<" Bytes"<<"\n";
    cout<<"SIZE OF BOOLEAN - "<<sizeof(bool)<<" Bytes"<<"\n";

    return 0;
}